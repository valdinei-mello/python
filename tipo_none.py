"""
Tipo None

O tipo de dado em python representa o tipo sem tipo, ou poderia ser conhecido também
como tipo vazio, porém, falar que é um tipo sem tipo é mais apropiado.

OBS: O tipo None é sempre especificado com a primeira letra em maiscula;
    O tipo None é considerado sempre falso;

Quando utilizamos?
    - Podemos utilizar o tipo None quando queremos inicializarmos uma variavel com tipo sem tipo, antes
    de recebermos um valor final.

"""

